//! typeof Operator in javaScript

// Some (Primitive data types) in js are 
// 1> string "utkarsh"
// 2> number 2, 4, 5, 6
// 3> undefined  
// 3> null
// 4> Bigint
// 5> Symbol


let age=22;
let firstName="utkarsh";
console.log(typeof(firstName));



// convert number to string  i.e 22->"22" todo this we can add empty string with var
age=age+"";
console.log(typeof(age));


// convert string to number
let ages="18";
ages=Number(ages);
console.log(typeof(ages));
